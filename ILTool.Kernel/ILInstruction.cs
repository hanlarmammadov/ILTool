    public class ILInstruction
    {
        public ByteCode ILOperation;
        public Object Operand;
        public string Label;
        private ITest _test;
        private object _data;

        public ILInstruction(ByteCode ilOperation, Object operand = null)
        {
            ILOperation = ilOperation;
            Operand = operand;
        }
        public ILInstruction AddLabel(string name)
        {
            Label = name.ToLower().Trim();
            return this;
        }
        public void AddCallback(ITest test)
        {
            _test = test;
        }

        public ILInstruction Data(object data)
        {
            _data = data;
            return this;
        }

        public void ExecuteCallback()
        {
            if (_test != null && _data != null)
                _test.Exec(_data);
        }
    }
